class PlayFunction:
    def __init__(self, views):
        self.views = views

class Film:
    def __init__(self, title, publication_year, genre, views):
        self.title = title
        self.publication_year = publication_year
        self.genre = genre
        self.views = views

class Serial:
    def __init__(self, title, publication_year, genre, e_number, s_number, views):
        self.title = title
        self.publication_year = publication_year
        self.genre = genre
        self.e_number = e_number
        self.s_number = s_number
        self.views = views




